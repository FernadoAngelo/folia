    NOME_ARQUIVO_RETORNO VARCHAR(100) NOT NULL,
    NUMERO_ARQUIVO VARCHAR(2) NOT NULL,
    DATA_ARQUIVO VARCHAR(8) NOT NULL,
    DATA_CREDITO_LANCAMENTO VARCHAR(8) NOT NULL,
    IMPRIMIR_MARGEM_PADRAO VARCHAR(2) NOT NULL,
    LECEDENTE_RETORNO VARCHAR(2) NOT NULL,
    REMOVE_ACENTOS_ARQUIVO_REMESSA VARCHAR(2) NOT NULL,
    PREFIXO_ARQUIVO_REMESSA VARCHAR(15) NOT NULL,

    PRIMARY KEY (ID)
);

