CREATE TABLE ENDERECO
(
    ID VARCHAR(36) NOT NULL,
    LOGRADOURO VARCHAR(80) NOT NULL,
    NUMERO_RESIDENCIAL VARCHAR(10) NOT NULL,
    COMPLEMENTO VARCHAR(50) NOT NULL,
    BAIRRO VARCHAR(50) NOT NULL,
    CIDADE VARCHAR(50) NOT NULL,
    CEP VARCHAR(8) NOT NULL,
    UF VARCHAR(2) NOT NULL,

    PRIMARY KEY (ID)
);

CREATE TABLE USUARIO
(
    ID   VARCHAR(36) NOT NULL,
    IDENTIFICACAO VARCHAR(10) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL,
    USUARIO VARCHAR(50) NOT NULL,
    SENHA VARCHAR(8) NOT NULL,
    TIPO_USUARIO VARCHAR(7) NOT NULL,
    ENDERECO_FK VARCHAR(36),

    PRIMARY KEY(ID),
    FOREIGN KEY (ENDERECO_FK) REFERENCES ENDERECO (ID)
);

CREATE TABLE EMPRESA
(
    ID VARCHAR(36) NOT NULL,
    NOME_FANTASIA VARCHAR(100) NOT NULL,
    RAZAO_SOCIAL VARCHAR(100) NOT NULL,
    CNPJ VARCHAR(14) NOT NULL,
    EMAIL VARCHAR(50) NOT NULL,
    SENHA VARCHAR(20) NOT NULL,
    ENDERECO_FK VARCHAR(36),

    PRIMARY KEY(ID),
    FOREIGN KEY (ENDERECO_FK) REFERENCES ENDERECO (ID)
);

CREATE TABLE ARQUIVO_BOLETO
(
    ID VARCHAR(36) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    DIRETORIO_LOGO VARCHAR(50) NOT NULL,
    FILTRO VARCHAR(50) NOT NULL,
    LAYOUT VARCHAR(50) NOT NULL,
    MOSTRAR_PREVIEW VARCHAR(50) NOT NULL,
    MOSTRAR_PROGRESSO VARCHAR(50) NOT NULL,
    MOSTRAR_SETUP VARCHAR(50) NOT NULL,
    NUMERO_COPIAS VARCHAR(50) NOT NULL,
    PRINTER_NOME VARCHAR(100) NOT NULL,
    SOFTWARE_HOUSE VARCHAR(100) NOT NULL,
    ALTERAR_ESCALA VARCHAR(50) NOT NULL,
    NOVA_ESCALA VARCHAR(50) NOT NULL,
    CALC_NOME_ARQ_PDF_IND VARCHAR(50) NOT NULL,
    MARGEM_SUP VARCHAR(50) NOT NULL,
    MARGEM_INF VARCHAR(50) NOT NULL,
    MARGEM_ESQ VARCHAR(50) NOT NULL,
    MARGEM_DIR VARCHAR(50) NOT NULL,

    PRIMARY KEY (ID)
);

CREATE TABLE BANCO
(
    ID VARCHAR(36) NOT NULL,
    AGENCIA VARCHAR(5) NOT NULL,
    AGENCIA_DIGITO VARCHAR(5) NOT NULL,
    CONTA VARCHAR(9) NOT NULL,
    CONTA_DIGITO VARCHAR(9) NOT NULL,
    DIGITO_VERIFICADOR VARCHAR(4) NOT NULL,

    PRIMARY KEY (ID)
);

CREATE TABLE BOLETO
(
    ID VARCHAR(36) NOT NULL,
    BANCO_ID VARCHAR(36) NOT NULL,
    CONVENIO VARCHAR(2) NOT NULL,
    CODIGO_CEDENTE VARCHAR(2) NOT NULL,
    CODIGO_TRANSMISSAO VARCHAR(2) NOT NULL,
    MODALIDADE VARCHAR(2) NOT NULL,
    RESPONSAVEL_EMISSAO VARCHAR(2) NOT NULL,
    TIPO_CARTEIRA VARCHAR(2) NOT NULL,
    TIPO_DOCUMENTO VARCHAR(2) NOT NULL,
    USUARIO_ID VARCHAR(36) NOT NULL,
    CARAC_TITULO VARCHAR(2) NOT NULL,
    CODIGO_NEGATIVACAO VARCHAR(2) NOT NULL,
    EMPRESA_ID VARCHAR(36) NOT NULL,
    IDENT_DISTRIBUICAO VARCHAR(2) NOT NULL,
    OPERACAO VARCHAR(30) NOT NULL,
    CHAVE_PIX VARCHAR(100) NOT NULL,
    TIPO_CHAVE_PIX VARCHAR(2) NOT NULL,

    PRIMARY KEY (ID),
    FOREIGN KEY (BANCO_ID) REFERENCES BANCO (ID),
    FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO (ID),
    FOREIGN KEY (EMPRESA_ID) REFERENCES EMPRESA (ID)
);

CREATE TABLE CONFIGURACAO_BANCO
(
    ID VARCHAR(36) NOT NULL,
    TIPO_COBRANCA VARCHAR(2) NOT NULL,
    NUMERO_CORRESPONDENTE VARCHAR(2) NOT NULL,
    LAYOUT_VERSAO_ARQUIVO VARCHAR(4) NOT NULL,
    LAYOUT_VERSAO_LOTE VARCHAR(4) NOT NULL,
    LOCAL_PAGAMENTO VARCHAR(50) NOT NULL,
    ORIENTACAO_BANCO VARCHAR(100) NOT NULL,
    DIGITO VARCHAR(4) NOT NULL,
    CASAS_DECIMAIS_MORA_JUROS VARCHAR(2) NOT NULL,
    CIP VARCHAR(50) NOT NULL,

    PRIMARY KEY (ID)
);

CREATE TABLE CREDENCIAL_BOLETO
(
    ID VARCHAR(36) NOT NULL,
    CLIENT_ID VARCHAR(20) NOT NULL,
    CLIENT_SECRET VARCHAR(20) NOT NULL,
    SCOPE VARCHAR(50) NOT NULL,
    INDICADOR_PIX VARCHAR(2) NOT NULL,

    PRIMARY KEY (ID)
);

CREATE TABLE DIRETORIO_BOLETO
(
    ID VARCHAR(36) NOT NULL,
    LAYOUT_REMESSA VARCHAR(2) NOT NULL,
    DIRETORIO_ARQUIVO_REMESSA VARCHAR(50) NOT NULL,
    DIRETORIO_ARQUIVO_RETORNO VARCHAR(50) NOT NULL,
